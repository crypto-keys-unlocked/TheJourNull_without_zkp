      async function connectWallet() {
        if (window.ethereum) {
            try {
                await window.ethereum.request({ method: 'eth_requestAccounts' });
                const provider = new ethers.providers.Web3Provider(window.ethereum);
                signer = provider.getSigner();
                console.log('Wallet connected');
            } catch (error) {
                console.error('Error connecting to MetaMask:', error);
            }
        } else {
            alert('MetaMask is not installed. Please install it to use this feature.');
        }
    }

    async function buyTokens(event) {
        event.preventDefault();
        if (!signer) {
            alert('Please connect to your wallet first.');
            return;
        }

        const tokenAmount = document.getElementById('tokenAmount').value;
        if (tokenAmount < 100 || tokenAmount > 1000) {
            alert('You can only buy between 100 and 1000 tokens.');
            return;
        }

        const contract = new ethers.Contract(contractAddress, contractABI, signer);
        try {
            const transaction = await contract.buyTokens({
                value: ethers.utils.parseEther((0.0001 * tokenAmount / 100).toString())
            });
            await transaction.wait();
            alert('Tokens purchased successfully!');
        } catch (error) {
            console.error('Purchase failed:', error);
            alert('There was an error processing your purchase.');
        }
    }

    connectButton.addEventListener('click', connectWallet);
